package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		// 8 primitive datatypes
        // to store whole numbers -- byte, short, int, long
		
		byte numberOne=127;
		short number2=32444;
		int numberThree=1000;
		long number4=2324444000l;
		
		//to store decimal numbers -- float, double
		
		float wholeSalePrice=44.89f;
		double retailPrice=64.899999999;
		
		//to store single character -- char
		
		char brandName='Z';
		
		//to store boolean values
		
		boolean thirsty=true;
		boolean hungry1=false;
		
		
		System.out.println(numberOne);
		
		
		
		
		
		
		
				
				
	}

}
