package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {
		
		// Create variables to store all different types of primitive data to
		//  test size range 
		
		// Print the value of each variable
		
		
		// 1 - numeric whole numbers 
		
		byte fieldOne=11;
		short fieldTwo=2300;
		int fieldThree=1110000;
		long fieldFour=230000000l;
		
		// 2 - numeric decimal numbers 
		
		float jerseyHome$=89.90f;
		double jerseyAway$=69.998877;
		
		// 3 - non-numeric single character
		
		char zoneA='A';
		
		// 4- non-numeric boolean 
		
		boolean zoneB=false;	
		
		System.out.println(fieldOne);
		System.out.println(fieldTwo);
		System.out.println(fieldThree);
		System.out.println(fieldFour);
		System.out.println(jerseyHome$);
		System.out.println(jerseyAway$);
		System.out.println(zoneA);
		System.out.println(zoneB);
		
		
		
		
		
		
		
		
		
	}
	
	
}

