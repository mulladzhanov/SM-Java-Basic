package com.syntax.class09;

public class morePatterns {

	public static void main(String[] args) {
		
		for (int a=1; a<=5; a++) {
			for (int b=1; b<=5; b++) {
				System.out.print(a);
			} System.out.println();
		}
System.out.println("----------------------");
	
	for (int i=1; i<=5; i++) {
		for (int j=5; j>=1; j--) {
			System.out.print(j);
		} System.out.println();
	}
	System.out.println();
	
	for (int i=5; i>=1; i--) {
		for (int j=5; j>=1; j--) {
			System.out.print(i);
		} System.out.println();
	}
	System.out.println("----------------------");
	
	
	
	
	
	
	}

}
