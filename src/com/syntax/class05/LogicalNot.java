package com.syntax.class05;

public class LogicalNot {

	public static void main(String[] args) {
		
		//NOT(!) - reverse the condition
		
		boolean b=true;
		boolean c=false;
		
		System.out.println(!b);
		System.out.println(!c);
		
		System.out.println("-----------------");
		
		
		
		
		
		
	}
	
}
