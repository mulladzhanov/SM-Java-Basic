package com.syntax.class07;

public class doWhileLoop {

	public static void main(String[] args) {
		
		int a=1;
		int b=1;
		
		do {
			System.out.println("Hello");
			a++;
		} while (a<=5);
			
		// while  
		System.out.println("-------");
		
		while (b<=5) {
			System.out.println("Hello");
			b++;
		}

	}

}
